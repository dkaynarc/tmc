package model;

public class AuthMessage
{

	private String Result;
	private String Name;

	public String getResult()
	{
		return Result;
	}

	public String getUserName()
	{
		return Name;
	}

	public void setResult(String result)
	{
		Result = result;
	}

	public void setUserName(String userName)
	{
		Name = userName;
	}

}
